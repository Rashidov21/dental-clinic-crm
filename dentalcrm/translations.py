def get_translation(text, language='uz'):
    """
    Get translation for a given text in the specified language.
    Falls back to the original text if translation is not found.
    """
    if language in TRANSLATIONS and text in TRANSLATIONS[language]:
        return TRANSLATIONS[language][text]
    return text
